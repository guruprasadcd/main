package demo;

public class calculator {

	public static void main(String[] args) {
		
		//WAP to print numbers from 1-10
	for(int i = 1;i<=10;i++)
		{
			System.out.println(i);
	}
		
		//WAT to print numbers from 10-1
		for(int j=10;j>=1;j--)
		{
			System.out.println(j);
		}

	}

}
